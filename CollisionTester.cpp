#include "CollisionTester.h"

bool CollisionTester::check_collision(SDL_Rect* A, SDL_Rect* B){
	SDL_Rect a = *A;
	SDL_Rect b = *B;

	float leftA, rightA, topA, bottomA;
	float leftB, rightB, topB, bottomB;

	//Calculate the sides of rect A
    leftA = a.x;
    rightA = a.x + a.w;
    topA = a.y;
    bottomA = a.y + a.h;
        
    //Calculate the sides of rect B
    leftB = b.x;
    rightB = b.x + b.w;
    topB = b.y;
    bottomB = b.y + b.h;

	//If any of the sides from A are outside of B
    if( bottomA <= topB )
    {
        return false;
    }
    
    if( topA >= bottomB )
    {
        return false;
    }
    
    if( rightA <= leftB )
    {
        return false;
    }
    
    if( leftA >= rightB )
    {
        return false;
    }
    
    //If none of the sides from A are outside B
    return true;
};